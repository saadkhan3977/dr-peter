        $data=$request->all();
        // $tags = $request->input('tags');
        // if($tags){
        //     $data['tags']=implode(',',$tags);
        // }
        // else{
        //     $data['tags']='';
        // }
        $slug=Str::slug($request->title);
        $count=Product::where('slug',$slug)->count();
        if($count>0){
            $slug=$slug.'-'.date('ymdis').'-'.rand(0,999);
        }
        $data['slug']=$slug;
